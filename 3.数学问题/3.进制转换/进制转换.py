int()#可以将二进制，八进制，十六进制转换成十进制整形

int('1111',2)
int('f',16)
int('17',8)

############################################
#将整型转换为字符：
chr()

##########################################
#将字符转换为整型：
ord()

##########################################
#将十进制转换为十六进制：
hex()

##########################################
#将十进制转换为八进制：
oct()

##########################################
#将十进制转换为二进制：
bin()

#返回的是字符串，去掉前两位即可



